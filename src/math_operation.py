# Function for addition
def add(a, b):
    return a + b

# Function for subtraction
def subtract(a, b):
    return a - b

# Calling the functions
x = 10
y = 5

sum_result = add(x, y)
sub_result = subtract(x, y)

# Printing the results
print("Addition:", sum_result)
print("Subtraction:", sub_result)

